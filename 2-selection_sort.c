/**
 * swap_ints - Swap two integers in an arrayss
 * @a: The first integer to swapss
 * @b: The second integer to swapss
 */

void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

